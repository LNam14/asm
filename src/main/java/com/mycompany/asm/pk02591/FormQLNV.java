    public void showData(List<NhanVien> nvl) {
        List<NhanVien> nvlist = new ArrayList<>();
        nvlist = nvl;
        tblTable.getModel();
        tblModel = (DefaultTableModel) tblTable.getModel();
        tblModel.setRowCount(0);
        nvlist.forEach((nv) -> {
            tblModel.addRow(new Object[]{
                nv.getMaNv(), nv.getHoTen(), nv.getTuoi(), nv.getEmail(), nv.getLuong(), nv.getPhongBan()
            });
        });
    }

