    private void btnnewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnewActionPerformed
        String tt = txthoTen.getText() + ";" + txtTuoi.getText() + ";" + txtEmail.getText()
                + ";" + txtLuong.getText() + ";" + txtPhongban.getText();
        QLNV.insert(tt);
        JOptionPane.showMessageDialog(null, "Da them");
        showData(QLNV.findAll());
    }//GEN-LAST:event_btnnewActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        NhanVien nv = new NhanVien();
        nv.setHoTen(txthoTen.getText());
        nv.setTuoi(Integer.parseInt(txtTuoi.getText()));
        nv.setHoTen(txtEmail.getText());
        nv.setTuoi(Integer.parseInt(txtLuong.getText()));
        nv.setHoTen(txtPhongban.getText());
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        showData(QLNV.findAll());
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        String key = JOptionPane.showInputDialog(null, "Nhap thong tin can tim kiem");
        if(key == null || key.equals("")) {
            showData(QLNV.findAll());
        } else showData(QLNV.find(key.toLowerCase()));
    }//GEN-LAST:event_btnfindActionPerformed

