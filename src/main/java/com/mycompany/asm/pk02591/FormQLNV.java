    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        // TODO add your handling code here:
        int d = tblTable.getSelectedRow();
        String mnv = tblTable.getValueAt(d, 0).toString();
        String ht = tblTable.getValueAt(d, 1).toString();
        String tuoi = tblTable.getValueAt(d, 2).toString();
        String email = tblTable.getValueAt(d, 3).toString();
        String luong = tblTable.getValueAt(d, 4).toString();
        String phongban = tblTable.getValueAt(d, 5).toString();
        txtmnv.setText(mnv);
        txthoTen.setText(ht);
        txtTuoi.setText(tuoi);
        txtEmail.setText(email);
        txtLuong.setText(luong);
        txtPhongban.setText(phongban);
    }//GEN-LAST:event_tblTableMouseClicked

